// Arithmetic Operator
// +, -, *, /, %

main(List<String> args) {
  var num1 = 10;
  var num2 = 20;

  print('Addition : ');
  print(num1 + num2);
  print('Substraction : ');
  print(num1 - num2);
  print('Multiplication : ');
  print(num1 * num2);
  print('Devision : ');
  print(num1 / num2);
  print('Modulo/Remender : ');
  print(num1 % num2);
}
