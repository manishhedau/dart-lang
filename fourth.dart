// const & final keyword
// const keyword is used make constant value permanataly in run time, so that the we can't change the value of this variable.

// final keyword is used to make variable constant for entier program.

main(List<String> args) {
  const f_name = 'Manish';
  final l_name = 'Hedau';

  print(f_name + ' ' + l_name);
}
