// datatype

// Dart

// 1. integer - num
// 2. float - double
// 3. boolean - bool

main(List<String> args) {
  var number1 = 10;
  num number2 = 20;

  print('Integer Data type');
  print(number1);
  print(number1);
  print('\n');

  var f1 = 1455.466;
  double f2 = 244.355;
  print('Float/Double Data type');
  print(f1);
  print(f2);
  print('\n');

  var boolean1 = true;
  bool boolean2 = false;

  print('Boolean Data type');
  print(boolean1);
  print(boolean2);
}
